package isotope.modules.security.config;

import org.springframework.security.core.AuthenticationException;
import org.springframework.security.web.AuthenticationEntryPoint;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

public class Http302AuthenticationEntryPoint implements AuthenticationEntryPoint {


    public Http302AuthenticationEntryPoint() {
    }

    @Override
    public void commence(HttpServletRequest request, HttpServletResponse response, AuthenticationException authExceptio) throws IOException, ServletException {
        response.sendRedirect("/");
    }

}

